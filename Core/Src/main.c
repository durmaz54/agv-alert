void Set_LED (int LEDnum, int Red, int Green, int Blue)
{
	LED_Data[LEDnum][0] = LEDnum;
	LED_Data[LEDnum][1] = Green;
	LED_Data[LEDnum][2] = Red;
	LED_Data[LEDnum][3] = Blue;
}

#define PI 3.14159265

void Set_Brightness (int brightness)  // 0-45
{
#if USE_BRIGHTNESS

	if (brightness > 45) brightness = 45;
	for (int i=0; i<MAX_LED; i++)
	{
		LED_Mod[i][0] = LED_Data[i][0];
		for (int j=1; j<4; j++)
		{
			float angle = 90-brightness;  // in degrees
			angle = angle*PI / 180;  // in rad
			LED_Mod[i][j] = (LED_Data[i][j])/(tan(angle));
		}
	}
#endif
}

uint16_t pwmData[(24*MAX_LED)+50];

void WS2812_Send (void)
{
	uint32_t indx=0;
	uint32_t color;


	for (int i= 0; i<MAX_LED; i++)
	{
#if USE_BRIGHTNESS
		color = ((LED_Mod[i][1]<<16) | (LED_Mod[i][2]<<8) | (LED_Mod[i][3]));
#else
		color = ((LED_Data[i][1]<<16) | (LED_Data[i][2]<<8) | (LED_Data[i][3]));
#endif

		for (int i=23; i>=0; i--)
		{
			if (color&(1<<i))
			{
				pwmData[indx] = 60;  // 2/3 of 90
			}

			else pwmData[indx] = 30;  // 1/3 of 90

			indx++;
		}

	}

	for (int i=0; i<50; i++)
	{
		pwmData[indx] = 0;
		indx++;
	}

	HAL_TIM_PWM_Start_DMA(&htim4, TIM_CHANNEL_1, (uint32_t *)pwmData, indx);
	while (!datasentflag){};
	datasentflag = 0;
}

uint16_t  effStep;

uint8_t Alarm_durum() {
    // Strip ID: 0 - Effect: Fade - LEDS: 60
    // Steps: 7.5 - Delay: 80
    // Colors: 2 (255.255.255, 255.0.0)
    // Options: duration=600, every=1,
 // if(millis() - effStart < 80 * (effStep)) return 0x00;
  uint8_t r,g,b;
  double e;
  e = (effStep * 80) / (double)600;
  r = ( e ) * 255 + 255 * ( 1.0 - e );
  g = ( e ) * 0 + 255 * ( 1.0 - e );
  b = ( e ) * 0 + 255 * ( 1.0 - e );
  for(int16_t j=0;j<60;j++) {
    if((j % 1) == 0){
      Set_LED(j, r, g, b);
    WS2812_Send();
    }
    else
    Set_LED(j, 0, 0, 0);
    WS2812_Send();
  }
  if(effStep >= 7.5) {effStep = 0; return 0x03; }
  else effStep++;
  return 0x01;
}




